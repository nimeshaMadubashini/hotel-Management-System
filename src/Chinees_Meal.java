public class Chinees_Meal {
    private String num;
    private String meal;

    public Chinees_Meal() {
    }

    public Chinees_Meal(String num, String meal) {
        this.num = num;
        this.meal = meal;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getMeal() {
        return meal;
    }

    public void setMeal(String meal) {
        this.meal = meal;
    }
}
